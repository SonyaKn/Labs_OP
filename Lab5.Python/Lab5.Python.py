
k=0
for i in range(10,100):
    a1 = i // 10
    a2 = i % 10
    for j in range(0,100):
        a3 = j // 10
        a4 = j % 10
        if ((a1==a3)and(a2==a4)):
            k+=1

print("Кількість чотирьохзначних чисел, що представляють собою повторювані фрагменти послідовності цифр, дорівнює: ", k)