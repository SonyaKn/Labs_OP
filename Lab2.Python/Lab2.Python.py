import math

x=int(input("Введіть сторону X : "))
y=int(input("Введіть сторону Y : "))
z=int(input("Введіть сторону Z : "))

if ((x <= 0) or (y <= 0) or (z <= 0)):
    print("Задані сторони не підходять для побудування трикутника.")
elif ((x + y > z) and (y + z > x) and (x + z > y)):
    print("Трикутник із заданими сторонами існує.")
else:
    print("Трикутник із заданими сторонами не існує.")
    